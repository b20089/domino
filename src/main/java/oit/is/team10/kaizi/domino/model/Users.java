package oit.is.team10.kaizi.domino.model;

public class Users {
  int id;
  int roomid;
  String userName;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return userName;
  }

  public void setName(String name) {
    this.userName = name;
  }

  public int getRoomid() {
    return roomid;
  }

  public void setRoomid(int roomid) {
    this.roomid = roomid;
  }

}
